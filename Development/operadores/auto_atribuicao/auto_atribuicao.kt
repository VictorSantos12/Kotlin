  var x: Int = 2
   var y: Int = 5

fun main() {

   println(x = y) // O operando x recebe o operando y
   println(x -= y) // O operando x recebe a subtracao entre os operandos x e y
   println(x /= y) // O operando x recebe a divisao entre os operandos x e y
   println(x %= y) // O operando x recebe o resto da divisao entre os operandos x e y
   println(x += y) // O operando x recebe a soma entre os operandos x e y
   println(x *= y) // O operando x recebe a multiplicacao entre os operandos x e y
}