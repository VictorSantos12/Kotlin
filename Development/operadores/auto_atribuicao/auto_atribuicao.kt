var x: Int = 2

fun main() {

   println("${2 = 5}")  // O operando x recebe o operando y
   println("${2 -= 5}") // O operando x recebe a subtracao entre os operandos x e y
   println("${2 /= 5}") // O operando x recebe a divisao entre os operandos x e y
   println("${2 %= 5}") // O operando x recebe o resto da divisao entre os operandos x e y
   println("${2 += 5}") // O operando x recebe a soma entre os operandos x e y
   println("${2 *= 5}") // O operando x recebe a multiplicacao entre os operandos x e y
}